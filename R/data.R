#' GC and Repeat Metrics for Genomic Intervals
#'
#' A data frame containing genomic intervals with computed sequence metrics
#' including GC content, repeat fraction, and a predicted coverage score based
#' on a linear model. This data frame was created using a vectorized approach
#' that extracts sequences from a reference FASTA file and uses RepeatMasker
#' annotation to compute the fraction of each interval covered by repeats.
#'
#' @format A data frame with 7 variables:
#' \describe{
#'   \item{seqnames}{A character vector indicating the chromosome or sequence name (e.g., "chr1").}
#'   \item{start}{An integer vector specifying the start coordinate of each genomic interval.}
#'   \item{end}{An integer vector specifying the end coordinate of each genomic interval.}
#'   \item{width}{An integer vector indicating the width of each interval (typically end - start + 1).}
#'   \item{gc_content}{A numeric vector representing the fraction of nucleotides that are either G or C in each interval.}
#'   \item{repeat_fraction}{A numeric vector giving the fraction of each interval that overlaps with annotated repeat regions.}
#'   \item{predicted_score}{A numeric vector containing the predicted coverage score for each interval, calculated from a linear model using \code{gc_content} and \code{repeat_fraction} as predictors.}
#' }
#'
#' @details This dataset is intended for use in analyses aiming to correct read
#' depth for sequence-specific biases. The \code{predicted_score} is derived from a
#' linear regression model of observed coverage against \code{gc_content} and
#' \code{repeat_fraction}. Such a model can be used to adjust raw copy number estimates.
#'
#' @source Generated by processing genomic coverage data, reference genome sequences,
#' and RepeatMasker annotations.
#'
#' @examples
#' # View the first few rows of the data frame
#' head(gc_repeat_data)
#'
#' # Plot a histogram of GC content
#' ggplot2::ggplot(gc_repeat_data, ggplot2::aes(x = gc_content)) +
#'   ggplot2::geom_histogram(bins = 2000)
#'
#' # Plot a histogram of repeat fraction
#' ggplot2::ggplot(gc_repeat_data, ggplot2::aes(x = repeat_fraction)) +
#'   ggplot2::geom_histogram(bins = 200)
#'
#' # Fit a linear model using gc_content and repeat_fraction to predict the score
#' mod <- lm(predicted_score ~ gc_content + repeat_fraction, data = gc_repeat_data)
#' summary(mod)
